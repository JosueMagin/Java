    private void btnAgregarDetalleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarDetalleMouseClicked
        // TODO add your handling code here:
        Empleado empleado = new Empleado();
        Prestamo prestamo = new Prestamo();
        prestamo.setIdPrestamo(Integer.parseInt(txtIdPrestamo.getText()));
        prestamo.setFechaPrestamo(txtFecha.getText());
        prestamo.setTotalAdecuado(Double.parseDouble(txtTotalAdecuado.getText()));
        empleado.setIdCliente(Integer.parseInt(txtIdCliente.getText()));
        empleado.setNombre(txtNombreCliente.getText());
        empleado.setNit(txtNitCliente.getText());
        empleado.setAbono(Double.parseDouble(txtAbono.getText()));
        detallePrestamo.calcularPrestamo();
        detallePrestamo.getLstdetalle().add(new Detalle_prestamo(empleado, prestamo));
        popularPrestamo();
        
        
    }//GEN-LAST:event_btnAgregarDetalleMouseClicked


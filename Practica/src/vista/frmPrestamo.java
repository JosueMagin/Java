    public void popularPrestamo(){
        String[] columnas = {"ID CLIENTE", "NOMBRE","NIT","ABONO","SALDO ACTUAL"};
        DefaultTableModel model = new DefaultTableModel(null, columnas);
        Object[] obj = new Object[5];
        for(Detalle_prestamo iterar : detallePrestamo.getLstdetalle()){
            obj[0] = iterar.getEmpleado().getIdCliente();
            obj[1] = iterar.getEmpleado().getNombre();
            obj[2] = iterar.getEmpleado().getNit();
            obj[3] = iterar.getEmpleado().getAbono();
            obj[4] = iterar.getSaldo();
            model.addRow(obj);
        }
        tblPrestamo.setModel(model);        
    }

